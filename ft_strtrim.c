#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	char	*s;
	int		start;
	int		end;

	if (s1 == NULL)
		return (NULL);
	if (set == NULL)
		return (ft_strdup(s1));
	start = 0;
	end = ft_strlen(s1) - 1;
	while (s1[start] && ft_strchr(set, s1[start]))
		start++;
	while (s1[end] && ft_strchr(set, s1[end]))
		end--;
	s = ft_substr(s1, start, end - start + 1);
	return (s);
}
